import { Schema, model } from "mongoose";
import normalize from "normalize-mongoose";

const contactSchema = new Schema({
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  subject: {
    type: String,
    required: true,
  },
  message: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

contactSchema.plugin(normalize);
export const ContactModel = model("Contact", contactSchema);
